function entrar(){
    let obj = document.getElementById('div1')
    obj.style.background = ('red')
    obj.innerText = ('Entrou')
}
function sair(){
    let obj = document.getElementById('div1')
    obj.style.background = ('gold')
    obj.innerText = ('Saiu')
}
function clickar(){
    let obj = document.getElementById('div1')
    obj.style.background = ('green')
    obj.innerText = ('Clicou')
}